package liuyuan210512;

public class SwitchCaseDemo {

	public static void main(String[] args) {
		 int  a = 2;
		switch (a) {
		case 1:
			System.out.println("1");
			break;
		case 3:
			System.out.println("3");
			break;
		case 4:
			System.out.println("4");
		default:
			System.out.println("there is no ");
			break;
		}

	}

}
